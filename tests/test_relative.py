import nldate
import pytest


@pytest.mark.parametrize(
    ("text", "today", "expected"),
    [
        ("tomorrow", date(2025, 12, 1), date(2025, 12, 2)),
        ("yesterday", date(2025, 12, 1), date(2025, 11, 30)),
        ("today", date(2025, 12, 1), date(2025, 12, 1)),
        ("in 3 days", date(2025, 12, 1), date(2025, 12, 4)),
        ("in 1 day", date(2025, 12, 1), date(2025, 12, 2)),
        ("in 2 weeks", date(2025, 12, 1), date(2025, 12, 15)),
        ("in 1 week", date(2025, 12, 1), date(2025, 12, 8)),
        ("in 1 month", date(2025, 1, 31), date(2025, 2, 28)),
        ("in 2 months", date(2025, 12, 1), date(2026, 2, 1)),
        ("in 1 year", date(2024, 2, 29), date(2025, 2, 28)),
        ("in 2 years", date(2025, 12, 1), date(2027, 12, 1)),
        ("in 1 week and 2 days", date(2025, 12, 1), date(2025, 12, 10)),
        ("in 1 year and 2 months", date(2025, 12, 1), date(2027, 2, 1)),
        ("in a day", date(2025, 12, 1), date(2025, 12, 2)),
        ("in a week", date(2025, 12, 1), date(2025, 12, 8)),
        ("in an hour", date(2025, 12, 1), date(2025, 12, 1)),
        ("in a month", date(2025, 1, 31), date(2025, 2, 28)),
        ("in a year", date(2024, 2, 29), date(2025, 2, 28)),
        ("in one week", date(2025, 12, 1), date(2025, 12, 8)),
        ("in two weeks", date(2025, 12, 1), date(2025, 12, 15)),
        ("in twenty-one days", date(2025, 12, 1), date(2025, 12, 22)),
        ("in a couple weeks", date(2025, 12, 1), date(2025, 12, 15)),
        ("in a couple of weeks", date(2025, 12, 1), date(2025, 12, 15)),
        ("in one month and two weeks", date(2025, 1, 31), date(2025, 3, 14)),
        ("in a year and a day", date(2024, 2, 29), date(2025, 3, 1)),
        ("3 days from now", date(2025, 12, 1), date(2025, 12, 4)),
        ("1 day from now", date(2025, 12, 1), date(2025, 12, 2)),
        ("3 weeks from now", date(2025, 12, 1), date(2025, 12, 22)),
        ("1 month from now", date(2025, 1, 31), date(2025, 2, 28)),
        ("2 years from now", date(2025, 12, 1), date(2027, 12, 1)),
        ("1 month and 3 days from now", date(2025, 1, 31), date(2025, 3, 3)),
        ("a week from now", date(2025, 12, 1), date(2025, 12, 8)),
        ("two months from now", date(2025, 12, 1), date(2026, 2, 1)),
        ("3 days ago", date(2025, 12, 1), date(2025, 11, 28)),
        ("2 weeks ago", date(2025, 12, 1), date(2025, 11, 17)),
        ("1 week ago", date(2025, 12, 1), date(2025, 11, 24)),
        ("1 month ago", date(2025, 3, 31), date(2025, 2, 28)),
        ("2 months ago", date(2025, 12, 1), date(2025, 10, 1)),
        ("1 year ago", date(2025, 2, 28), date(2024, 2, 28)),
        ("2 years ago", date(2025, 12, 1), date(2023, 12, 1)),
        ("1 month and 3 days ago", date(2025, 3, 31), date(2025, 2, 25)),
        ("a day ago", date(2025, 12, 1), date(2025, 11, 30)),
        ("a week ago", date(2025, 12, 1), date(2025, 11, 24)),
        ("an hour ago", date(2025, 12, 1), date(2025, 12, 1)),
        ("a month ago", date(2025, 3, 31), date(2025, 2, 28)),
        ("a year ago", date(2025, 2, 28), date(2024, 2, 28)),
        ("one week ago", date(2025, 12, 1), date(2025, 11, 24)),
        ("two weeks ago", date(2025, 12, 1), date(2025, 11, 17)),
        ("twenty-one days ago", date(2025, 12, 1), date(2025, 11, 10)),
        ("a couple weeks ago", date(2025, 12, 1), date(2025, 11, 17)),
        ("a couple of weeks ago", date(2025, 12, 1), date(2025, 11, 17)),
        ("a month and three days ago", date(2025, 3, 31), date(2025, 2, 25)),
        ("two years and six months ago", date(2025, 12, 1), date(2023, 6, 1)),
        ("  In   2   Weeks  ", date(2025, 12, 1), date(2025, 12, 15)),
        ("next monday", date(2025, 12, 1), date(2025, 12, 8)),
        ("Next Monday", date(2025, 12, 1), date(2025, 12, 8)),
        ("next Tuesday", date(2025, 12, 1), date(2025, 12, 2)),
        ("last friday", date(2025, 12, 1), date(2025, 11, 28)),
        ("next Tuesday", date(2025, 12, 30), date(2026, 1, 6)),
        ("tomorrow", date(2024, 2, 28), date(2024, 2, 29)),
    ],
)
def test_relative_dates_use_explicit_today(text, today, expected):
    assert nldate.parse(text, today=today) == expected
